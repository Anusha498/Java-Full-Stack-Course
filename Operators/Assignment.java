public class Assignment {
    public static void main(String[] args) {
        //Compound Assignment Operators
       int num = 10;
       num += 5; //10+5
       System.out.println("Number is:"+num);

       num *= 5; //15*5
       System.out.println("Number is:"+num); 

       num -= 5; //75-5
       System.out.println("Number is:"+num);

       num /= 5; //70/5
       System.out.println("Number is:"+num);

       num %= 5; //14%5
       System.out.println("Number is:"+num);

       
    }

}
